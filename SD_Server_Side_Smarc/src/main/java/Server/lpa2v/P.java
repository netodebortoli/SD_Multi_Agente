package Server.lpa2v;

class P {

    private double mi;
    private double lambda;

    public P(double mi, double lambda) {
        this.mi = mi;
        this.lambda = lambda;
    }

    public double getMi() {
        return mi;
    }

    public void setMi(double mi) {
        this.mi = mi;
    }

    public double getLambda() {
        return lambda;
    }

    public void setLambda(double lambda) {
        this.lambda = lambda;
    }
}
